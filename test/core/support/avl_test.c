  gpr_avl_unref(avl, NULL);

  check_negget(avl3, 3);
  check_get(avl3, 4, 2);
  check_get(avl3, 5, 3);
  gpr_avl_unref(avl3, NULL);

  check_get(avl4, 3, 1);
  check_negget(avl4, 4);
