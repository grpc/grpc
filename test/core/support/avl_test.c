static void test_remove(void) {
  gpr_avl avl;
  gpr_avl avl3, avl4, avl5, avln;
  gpr_log(GPR_DEBUG, "test_remove");
  avl = gpr_avl_create(&int_int_vtable);
  avl = gpr_avl_add(avl, box(3), box(1));
  avl = gpr_avl_add(avl, box(4), box(2));
  avl = gpr_avl_add(avl, box(5), box(3));

  avl3 = remove_int(gpr_avl_ref(avl), 3);
  avl4 = remove_int(gpr_avl_ref(avl), 4);
  avl5 = remove_int(gpr_avl_ref(avl), 5);
  avln = remove_int(gpr_avl_ref(avl), 1);

  gpr_avl_unref(avl);

  check_negget(avl3, 3);
  check_get(avl3, 4, 2);
  check_get(avl3, 5, 3);
  gpr_avl_unref(avl3);

  check_get(avl4, 3, 1);
  check_negget(avl4, 4);
  check_get(avl4, 5, 3);
  gpr_avl_unref(avl4);

  check_get(avl5, 3, 1);
  check_get(avl5, 4, 2);
  check_negget(avl5, 5);
  gpr_avl_unref(avl5);

  check_get(avln, 3, 1);
  check_get(avln, 4, 2);
  check_get(avln, 5, 3);
  gpr_avl_unref(avln);
}

